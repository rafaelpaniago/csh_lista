// Variáveis

int numero01 = 30;
int numero02 = 3;

// Divisão
Console.WriteLine(numero01/numero02);

// Multiplicação
Console.WriteLine(numero01*numero02);

// Subtração
Console.WriteLine(numero01-numero02);

// Soma
Console.WriteLine(numero01+numero02);

// Módulo (resto da divisão)
Console.WriteLine(numero01%numero02);

// Incrementador
numero01++; // Pego meu valor 30 e acrescento + 1, retornando 31
Console.WriteLine(numero01);

// Decrementador
numero02--; // Pego meu valor 3 e tiro 1, retornando 2
Console.WriteLine(numero02);
