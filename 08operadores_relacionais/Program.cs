// Variáveis

int num01 = 5;
int num02 = 8;

// MAIOR
// Quando faço um comparativo o retorno é true ou false
Console.WriteLine(num01 > num02);

// MAIOR OU IGUAL
Console.WriteLine(num02 >= num01);

// MENOR 
Console.WriteLine(num01 < num02);

// MENOR OU IGUAL
Console.WriteLine(num02 <= num01);

// IGUAL
Console.WriteLine(num01 == num02);

// DIFERENTE
Console.WriteLine(num01 != num02);

